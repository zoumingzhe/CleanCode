from ztools import filebase
# ----------------------------------------------------------------------------------------------------
class scan(filebase):
    """
    scan类提供扫描器支持。
    """
    def __init__(self):
        self.__version = "0.1"
        self.__path = {}
# ----------------------------------------------------------------------------------------------------
    def version(self, isShow = False):
        """
        版本显示：
        输入参数：isShow = False
        返回参数：self.__version
        说明：调用该方法将返回类的版本号，若isShow == True则会在屏幕上打印版本号。
        """
        if(isShow):
            print("[CleanCode]-[scan]-[vesion:%s]" % self.__version)
        return self.__version
